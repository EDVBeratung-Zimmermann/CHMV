    private void jButtonErstellenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonErstellenActionPerformed
        // TODO add your handling code here:
        if (conn != null) {
            try {
                SQLAnfrage = conn.createStatement();
            } catch (SQLException ex) {
                Logger.getLogger(_DlgTabellenLeeren.class.getName()).log(Level.SEVERE, null, ex);
            }
        if (JOptionPane.showConfirmDialog(null,
                "Soll die Datenbank wirklich erstellt werden?",
                "Bestätigung",
                JOptionPane.YES_NO_OPTION) == 0) {
            try {
                SQL = "CREATE DATABASE IF NOT EXISTS milesverlag";
                if (jCheckBoxDatenbank.isSelected()) {
                    SQLAnfrage.executeUpdate(SQL);
                    Modulhelferlein.Infomeldung("Datenbank milesverlag wurde erstellt");
                } else {
                }
            } catch (SQLException exept) {
                Modulhelferlein.Fehlermeldung("Fehler beim Erstellen der Datenbank:" + exept.getMessage());
            }
        }
        this.dispose();
        }
    }//GEN-LAST:event_jButtonErstellenActionPerformed

