    private void DruckenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DruckenActionPerformed
        // TODO add your handling code here:
        java.util.Date vonDate = null;
        vonDate = field_von.getDate();
        java.util.Date bisDate = null;
        bisDate = field_bis.getDate();
        String strVon = "1970-01-01";
        String strBis = "1970-01-01";
        if (vonDate != null) {
            strVon = Modulhelferlein.printDateFormat("yyyy-MM-dd", vonDate);
        }
        if (bisDate != null) {
            strBis = Modulhelferlein.printDateFormat("yyyy-MM-dd", bisDate);
        }

        if (rbMiles.isSelected()) {
            if (rbAutor.isSelected()) {
                if (PDF.isSelected()) {               // Format PDF
                    berVerkaufMiles.bericht("Autor", "PDF", strVon, strBis);
                    //berVerkaufMilesAutor.berichtPDF(strVon, strBis);
                } else if (XLS.isSelected()) {        // Format XLSX
                    berVerkaufMiles.bericht("Autor", "XLS", strVon, strBis);
                    // berVerkaufMilesAutor.berichtXLS(strVon, strBis);
                } else {                              // Format DOCX
                    berVerkaufMiles.bericht("Autor", "DOC", strVon, strBis);
                    // berVerkaufMilesAutor.berichtDOC(strVon, strBis);
                }                                     // if
            } else if (rbISBN.isSelected()) {                                  // buch is selected
                if (PDF.isSelected()) {               // Format PDF
                    berVerkaufMiles.bericht("ISBN", "PDF", strVon, strBis);
                    // berVerkaufMilesBuch.berichtPDF(strVon, strBis);
                } else if (XLS.isSelected()) {        // Format XLSX
                    berVerkaufMiles.bericht("ISBN", "XLS", strVon, strBis);
                    // berVerkaufMilesBuch.berichtXLS(strVon, strBis);
                } else {                              // Format DOCX
                    berVerkaufMiles.bericht("ISBN", "DOC", strVon, strBis);
                    // berVerkaufMilesBuch.berichtDOC(strVon, strBis);
                }                                     // if Format
            } else {
                if (PDF.isSelected()) {               // Format PDF
                    berVerkaufMiles.bericht("Titel", "PDF", strVon, strBis);
                } else if (XLS.isSelected()) {        // Format XLSX
                    berVerkaufMiles.bericht("Titel", "XLS", strVon, strBis);
                } else {                              // Format DOCX
                    berVerkaufMiles.bericht("Titel", "DOC", strVon, strBis);
                }                                     // if Format
            } // if sortierung
        } else if (rbBOD.isSelected()) {
            if (sFilePathAndName.length() == 0) {
                Modulhelferlein.Fehlermeldung("Sie haben keine Datei ausgewählt!");
            } else {
                if (rbAutor.isSelected()) {
                    if (PDF.isSelected()) {           // Format PDF
                        berVerkaufBOD.bericht(sFilePathAndName, "Autor", "PDF", strVon, strBis);
                    } else if (XLS.isSelected()) {    // Format XLSX
                        berVerkaufBOD.bericht(sFilePathAndName, "Autor", "XLS", strVon, strBis);
                    } else {                          // Format DOCX
                        berVerkaufBOD.bericht(sFilePathAndName, "Autor", "DOC", strVon, strBis);
                    }                                 // if Format
                } else if (rbISBN.isSelected()) {                              // buch is selected
                    if (PDF.isSelected()) {           // Format PDF
                        berVerkaufBOD.bericht(sFilePathAndName, "ISBN", "PDF", strVon, strBis);
                    } else if (XLS.isSelected()) {    // Format XLSX
                        berVerkaufBOD.bericht(sFilePathAndName, "ISBN", "XLS", strVon, strBis);
                    } else {                          // Format DOCX
                        berVerkaufBOD.bericht(sFilePathAndName, "ISBN", "DOC", strVon, strBis);
                    }                                 // if Format
                } else {
                    if (PDF.isSelected()) {           // Format PDF
                        berVerkaufBOD.bericht(sFilePathAndName, "Titel", "PDF", strVon, strBis);
                    } else if (XLS.isSelected()) {    // Format XLSX
                        berVerkaufBOD.bericht(sFilePathAndName, "Titel", "XLS", strVon, strBis);
                    } else {                          // Format DOCX
                        berVerkaufBOD.bericht(sFilePathAndName, "Titel", "DOC", strVon, strBis);
                    }                                 // if Format
                }// if Sortierung
            }
        } else if (rbGesamt.isSelected()) {   // berVerkaufGesamt
            if (sFilePathAndName.length() == 0) {
                Modulhelferlein.Fehlermeldung("Sie haben keine Datei ausgewählt!");
            } else {
                if (rbAutor.isSelected()) {
                    if (PDF.isSelected()) {           // Format PDF
                        berVerkaufGesamt.bericht(sFilePathAndName, "Autor", "PDF", strVon, strBis);
                    } else if (XLS.isSelected()) {    // Format XLSX
                        berVerkaufGesamt.bericht(sFilePathAndName, "Autor", "XLS", strVon, strBis);
                    } else {                          // Format DOCX
                        berVerkaufGesamt.bericht(sFilePathAndName, "Autor", "DOC", strVon, strBis);
                    }                                 // if Format
                } else if (rbISBN.isSelected()) {                              // buch is selected
                    if (PDF.isSelected()) {           // Format PDF
                        berVerkaufGesamt.bericht(sFilePathAndName, "ISBN", "PDF", strVon, strBis);
                    } else if (XLS.isSelected()) {    // Format XLSX
                        berVerkaufGesamt.bericht(sFilePathAndName, "ISBN", "XLS", strVon, strBis);
                    } else {                          // Format DOCX
                        berVerkaufGesamt.bericht(sFilePathAndName, "ISBN", "DOC", strVon, strBis);
                    }                                 // if Format
                } else {
                    if (PDF.isSelected()) {           // Format PDF
                        berVerkaufGesamt.bericht(sFilePathAndName, "Titel", "PDF", strVon, strBis);
                    } else if (XLS.isSelected()) {    // Format XLSX
                        berVerkaufGesamt.bericht(sFilePathAndName, "Titel", "XLS", strVon, strBis);
                    } else {                          // Format DOCX
                        berVerkaufGesamt.bericht(sFilePathAndName, "Titel", "DOC", strVon, strBis);
                    }                                 // if Format
                }
            }
        }                                             // if Umfang
        this.dispose();
    }//GEN-LAST:event_DruckenActionPerformed

