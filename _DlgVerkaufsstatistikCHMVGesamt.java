    private void btnSalesBODActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalesBODActionPerformed
        // TODO add your handling code here:
        chooser.setFileFilter(new ModulMyFilter(".csv"));
        chooser.setAcceptAllFileFilterUsed(false);

        int Ergebnis = chooser.showDialog(null, "SALES.CSV - Datei von BoD wählen");

        if (Ergebnis == JFileChooser.APPROVE_OPTION) {
            try {
                sFilePathAndName = chooser.getSelectedFile().getCanonicalPath();
                lblSalesCSV.setText(sFilePathAndName);
            } catch (IOException e) {
                Modulhelferlein.Fehlermeldung("IO-Exception: " + e.getMessage());
            }    // try
        }        // if
    }//GEN-LAST:event_btnSalesBODActionPerformed

